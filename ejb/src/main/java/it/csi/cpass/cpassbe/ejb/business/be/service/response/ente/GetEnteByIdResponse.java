package it.csi.cpass.cpassbe.ejb.business.be.service.response.ente;

import it.csi.cpass.cpassbe.ejb.business.be.service.response.base.BaseGetResponse;
import it.csi.cpass.cpassbe.lib.dto.Ente;

/**
 * Response for reading Ente by its id.
 */
public class GetEnteByIdResponse extends BaseGetResponse<Ente> {

	private Ente ente;

	/**
	 * @return the ente
	 */
	public Ente getEnte() {
		return ente;
	}

	/**
	 * @param ente the ente to set
	 */
	public void setEnte(Ente ente) {
		this.ente = ente;
	}

	@Override
	protected Ente getEntity() {
		return ente;
	}

}
