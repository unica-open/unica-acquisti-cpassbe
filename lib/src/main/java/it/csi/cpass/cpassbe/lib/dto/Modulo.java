package it.csi.cpass.cpassbe.lib.dto;

import java.io.Serializable;

/**
 * The Class Modulo.
 */
public class Modulo extends BaseDto<Long> implements Serializable {

	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1L;

	/** The codice. */
	private String codice;
	/** The descrizione. */
	private String descrizione;

	/** Empty constructor */
	public Modulo() {}

	/**
	 * Constructor
	 * @param id the id
	 */
	public Modulo(Long id) {
		super(id);
	}

	/**
	 * Constructor
	 * @param id the id
	 * @param codice the codice
	 * @param descrizione the descrizione
	 */
	public Modulo(Long id, String codice, String descrizione) {
		super(id);
		this.codice = codice;
		this.descrizione = descrizione;
	}

	/**
	 * Gets the descrizione.
	 * @return the descrizione
	 */
	public String getDescrizione() {
		return descrizione;
	}

	/**
	 * Sets the descrizione.
	 * @param descrizione the new descrizione
	 */
	public void setDescrizione(String descrizione) {
		this.descrizione = descrizione;
	}

	/**
	 * @return the codice
	 */
	public String getCodice() {
		return codice;
	}

	/**
	 * @param codice the codice to set
	 */
	public void setCodice(String codice) {
		this.codice = codice;
	}


	@Override
	public String toString() {
		return new StringBuilder()
			.append("Modulo [codice=").append(codice)
			.append(", descrizione=").append(descrizione)
			.append(", id=").append(id)
			.append("]")
			.toString();
	}

}
